    private void handleChooseVoucher(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_handleChooseVoucher
        String eventNameValue = eventNameInput.getText();
        if (eventNameValue.trim().isBlank()) {
            JOptionPane.showMessageDialog(null, "Please enter event name of voucher !");
            return;
        }
        
        ArrayList<Voucher> vouchersList = CustomerController.getVoucherByEvent(eventNameValue);
        for (Voucher voucher : vouchersList) {
            System.out.println(voucher);
        }
    }//GEN-LAST:event_handleChooseVoucher

