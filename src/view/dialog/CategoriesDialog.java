    private void renderTable() {
        resultForProduct.setText(resultForString);
        DefaultTableModel defaultTableModel = (DefaultTableModel) resultTableProduct.getModel();
        for (Category dataItem : data) {
            Object[] data = { dataItem.getCategoryName(), dataItem.getDescription() };
            defaultTableModel.addRow(data);
        }
        ListSelectionModel listSelectionModel = resultTableProduct.getSelectionModel();
        listSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int selectedCount = listSelectionModel.getMinSelectionIndex();
                categoryChoosing = data.get(selectedCount);
                int categoryID = categoryChoosing.getCategoryID();
                idDisplay.setText(categoryID + "");

            }
        });
    }

