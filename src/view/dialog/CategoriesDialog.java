    private void handleComfirm(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_handleComfirm
        if (categoryChoosing == null) {
            JOptionPane.showMessageDialog(null, "Please select a category");
            return;
        }
        
        switch (currentUsing) {
            case "PRODUCT_CATEGORY": {
                productCategoryView.setCurrentCategory(categoryChoosing);
                this.dispose();
                break;
            }
            case "ADD_PRODUCT": {
                addProductDialog.setCurrentCategory(categoryChoosing);
                this.dispose();
                break;
            }
        }
    }//GEN-LAST:event_handleComfirm

