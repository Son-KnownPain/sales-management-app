    private void handleConfirm(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_handleConfirm
        String productName = this.inputValue.getText().trim();
        if (productName.isBlank()) {
            JOptionPane.showMessageDialog(null, "Input must be not empty");
            return;
        }
        ArrayList<Product> products = GeneralController.getProductsWithInput(productName);
        ProductsDialog dialog = new ProductsDialog(new javax.swing.JFrame(), true);
        dialog.setLocationRelativeTo(null);
        dialog.renderProduct(productName, products, view);
        this.dispose();
        dialog.setVisible(true);
    }//GEN-LAST:event_handleConfirm

